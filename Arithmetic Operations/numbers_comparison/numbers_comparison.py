""" A program to compare two numbers and determine their relationship. """
# Reading the two numbers from the STDN
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Using Conditional Statements to compare between the two numbers
# If they are equal
if num1 == num2:
        print("Both numbers are equal.")
# One of them is greater than the other
elif num1 > num2:
    print(f"{num1} is greater than {num2}.")
# One of them is less than the other
else:
    print(f"{num1} is less than {num2}.")



